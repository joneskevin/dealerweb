package com.ava.domain.vo;

import com.ava.domain.entity.Menu;
import com.ava.util.MyBeanUtils;

public class MenuVO {
	
	private java.lang.Integer id;

	private java.lang.String name;
	
	private java.lang.String description;
	
	private java.lang.String links;
	
	private java.lang.String rightsCode;
	
	private java.lang.Integer sortNo;
	
	/**是否有参数*/
	private java.lang.Integer isParameter;
	
	public MenuVO() {
	}
	
	public MenuVO(Menu menu) {
		if (menu != null) {
			MyBeanUtils.copyPropertiesContainsDate(this, menu);
		}
	}
	
	public java.lang.Integer getId() {
		return id;
	}

	public void setId(java.lang.Integer id) {
		this.id = id;
	}

	public java.lang.String getName() {
		return name;
	}

	public void setName(java.lang.String name) {
		this.name = name;
	}

	public java.lang.String getDescription() {
		return description;
	}

	public void setDescription(java.lang.String description) {
		this.description = description;
	}

	public java.lang.String getRightsCode() {
		return rightsCode;
	}

	public void setRightsCode(java.lang.String rightsCode) {
		this.rightsCode = rightsCode;
	}

	public java.lang.String getLinks() {
		return links;
	}

	public void setLinks(java.lang.String links) {
		this.links = links;
	}

	public java.lang.Integer getIsParameter() {
		return isParameter;
	}

	public void setIsParameter(java.lang.Integer isParameter) {
		this.isParameter = isParameter;
	}

	public java.lang.Integer getSortNo() {
		return sortNo;
	}

	public void setSortNo(java.lang.Integer sortNo) {
		this.sortNo = sortNo;
	}


}

package com.ava.domain.vo;

public class TestDriveCarCount {

	private String regionName;
	
	private String testDriveRate;
	
	private Integer testDriveCount;
	
	private Integer violationCount;
	
	private Integer customersCount;

	public String getRegionName() {
		return regionName;
	}

	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}

	public String getTestDriveRate() {
		return testDriveRate;
	}

	public void setTestDriveRate(String testDriveRate) {
		this.testDriveRate = testDriveRate;
	}

	public Integer getTestDriveCount() {
		return testDriveCount;
	}

	public void setTestDriveCount(Integer testDriveCount) {
		this.testDriveCount = testDriveCount;
	}

	public Integer getViolationCount() {
		return violationCount;
	}

	public void setViolationCount(Integer violationCount) {
		this.violationCount = violationCount;
	}

	public Integer getCustomersCount() {
		return customersCount;
	}

	public void setCustomersCount(Integer customersCount) {
		this.customersCount = customersCount;
	}

}

package com.ava.domain.vo;

public class VehicleConfigCount{

	private Integer regionId;
	
	private String regionName;
	
	private String carTypeName;
	
	private Integer requireCount;

	private Integer optionCount;
	
	private Integer installCount;
	
	private Integer uninstallCount;

	public Integer getRegionId() {
		return regionId;
	}

	public void setRegionId(Integer regionId) {
		this.regionId = regionId;
	}

	public String getRegionName() {
		return regionName;
	}

	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}

	public String getCarTypeName() {
		return carTypeName;
	}

	public void setCarTypeName(String carTypeName) {
		this.carTypeName = carTypeName;
	}

	public Integer getRequireCount() {
		return requireCount;
	}

	public void setRequireCount(Integer requireCount) {
		this.requireCount = requireCount;
	}

	public Integer getOptionCount() {
		return optionCount;
	}

	public void setOptionCount(Integer optionCount) {
		this.optionCount = optionCount;
	}

	public Integer getInstallCount() {
		return installCount;
	}

	public void setInstallCount(Integer installCount) {
		this.installCount = installCount;
	}

	public Integer getUninstallCount() {
		return uninstallCount;
	}

	public void setUninstallCount(Integer uninstallCount) {
		this.uninstallCount = uninstallCount;
	}
}

package com.ava.domain.vo;

import com.ava.util.TypeConverter;


public class ProposalFindVO extends BaseFindVO {
	
	/** 申请ID */
	private java.lang.Integer proposalId;

	/** 联系人名称*/
	private java.lang.String contactName;
	
	/** 联系电话*/
	private java.lang.String contactPhone;
	
	/** 申请时间）*/
	private java.util.Date proposalTime;
	
	/** 状态：审批中、已通过、未通）*/
	private java.lang.Integer status;
	
	/** 状态中文名 */
	private String statusNick;
	
	/** 限购城市ID */
	private Integer restrictionCityId;
	
	public java.lang.Integer getProposalId() {
		return proposalId;
	}

	public void setProposalId(java.lang.Integer proposalId) {
		this.proposalId = proposalId;
	}

	public java.lang.String getContactName() {
		return contactName;
	}

	public void setContactName(java.lang.String contactName) {
		this.contactName = contactName;
	}

	public java.lang.String getContactPhone() {
		return contactPhone;
	}

	public void setContactPhone(java.lang.String contactPhone) {
		this.contactPhone = contactPhone;
	}

	public java.util.Date getProposalTime() {
		return proposalTime;
	}

	public void setProposalTime(java.util.Date proposalTime) {
		this.proposalTime = proposalTime;
	}

	public java.lang.Integer getStatus() {
		return status;
	}

	public void setStatus(java.lang.Byte status) {
		this.status = TypeConverter.toInteger(status);
	}

	public String getStatusNick() {
		return statusNick;
	}

	public void setStatusNick(String statusNick) {
		this.statusNick = statusNick;
	}

	public Integer getRestrictionCityId() {
		return restrictionCityId;
	}

	public void setRestrictionCityId(Integer restrictionCityId) {
		this.restrictionCityId = restrictionCityId;
	}


	
}

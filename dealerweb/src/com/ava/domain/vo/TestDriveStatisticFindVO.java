package com.ava.domain.vo;

import java.math.BigDecimal;

import com.ava.util.TypeConverter;


public class TestDriveStatisticFindVO extends BaseFindVO {
	
	/** 线路ID */
	private java.lang.Integer lineId;
	
	/** 线路名称*/
	private java.lang.String lineNick;

	/** 总次数*/
	private java.lang.Integer loopTotal;
	
	/** 总里程 */
	private java.lang.Integer mileageTotal;
	

	public java.lang.Integer getLineId() {
		return lineId;
	}

	public void setLineId(java.lang.Integer lineId) {
		this.lineId = lineId;
	}

	public java.lang.String getLineNick() {
		return lineNick;
	}

	public void setLineNick(java.lang.String lineNick) {
		this.lineNick = lineNick;
	}

	public java.lang.Integer getLoopTotal() {
		return loopTotal;
	}

	public void setLoopTotal(BigDecimal loopTotal) {
		this.loopTotal = TypeConverter.toInteger(loopTotal);
	}

	public java.lang.Integer getMileageTotal() {
		return mileageTotal;
	}

	public void setMileageTotal(BigDecimal mileageTotal) {
		this.mileageTotal = TypeConverter.toInteger(mileageTotal);
	}


}
